/**
 * Sentencia SWITCH
 */

let mes = 8;

switch (mes) {
  case 1:
    console.log('Enero');
    break;
  case 2:
    console.log('Febrero');
    break;
  case 3:
    console.log('Marzo');
    break;
  case 4:
    console.log('Abril');
    break;
  case 5:
    console.log('Mayo');
    break;
  default:
    console.log('Mes no encontrado');
}
